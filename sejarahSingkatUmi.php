                            <p style="margin-left:50px;text-align:justify;margin-right:50px">
                                Berdirinya Universitas Methodist Indonesia Medan (UMI) tidak terlepas dari prakarsa seorang warga jemaat Alm. Drs.F.Hutagalung yang dicetuskannya pada konferensi tahunan GMI tanggal 29 Januari 1969 yang berlangsung di Medan dari tanggal 26 sampai tanggal 31 Januari 1945, dan prakarsa tersebut mendapat sambutan positif dan merupakan salah satu keputusan Komperensi GMI tersebut.

                                Dengan berbekal fasilitas yang sangat minim, maka pada tanggal 1 Agustus 1965 berdirilah Perguruan Tinggi Methodist Indonesia Medan yang disingkat dengan PTMI yang saat itu perkuliahan dilaksanakan di Pergurun Kristen Methodist Indonesia di Jl.Thamrin No.58 (PKMI 2 sekarang).

                                Seiring dengan pesatnya minat masyarakat yang ingin melanjutkan pendidikan di Perguruan Tinggi maka pihak pengelola PTMI melihat peluang ini untuk menambah fakultasnya.maka pada tahun 1967 PTMI membuka Fakultas Jurusan Mesin. Setelah melihat banyaknya dukungan dari masyarakat maka pihak pengelola PTMI terus berupaya memacu pengembangan fisik berupa fasilitas pendidikan dan peningkatan statu. Pada tahun 1968 PTMI kembali membuka Fakultas Kedokteran, dan kedua fakultas ini terdaftar di PTIP dengan surat no.156A/DPT/I/1969 tanggal 4 september 1969. Maka sesuai undang-undang nomor 22 tahun 1961 tentang perguruan tinggi yang dikelola pihak PTMI dengan persyaratan 2 fakultas eksakta dan 2 fakultas non eksakta maka Perguruan Tinggi Methodist Indonesia (PTMI) berubah menjadi Universitas Methodist Indonesia Medan yang disingkat dengan UMI Medan pada tanggal 4 September 1969.

                                Setelah memenuhi standart untuk Universitas maka melalui konferensi Agung 1 Gereja Methodist Indonesia Medan tanggal 12 sampai dengan 16 February 1969 ditetapkan bahwa kampus UMI Medan berlokasi di Jl.Hang Tuah No.8 Medan.dari pertumbuhan Universitas Methodist Indonesia dapat terlihat dalam sejarah singkat diatas, maka dalam rangka pengembangan selanjutnya diperlukan suatu program pembangunan menyeluruh dan terpadu.

                                Awal perwujudan tersebut ditandai dengan diresmikannya gedung kampus II yang baru pada tanggal 22 Juni 1993 yang terletak di Jl.Setia Budi pasar II Tanjung Sari Medan, dan kegiatan perkuliahan Fakultas Kedokteran dan Fakultas Pertanian di pindahkan ke kampus II. Pada tahun 2001/2002 pihak Universitas membuka Diploma 3 (D-3) dengan program studi Manajemen Informatika dan Komputerisasi Akuntansi.

                                Pada akhir genap semester T.A 2004/2005 Universitas Methodist Indonesia Medan kembali membuka satu Fakultas baru yaitu Fakultas Ilmu Komputer dengan mengasuh dua program studi Teknik Informatika dan Sistem Informasi. Hal ini dengan surat Departemen Pendidikan Nasional No.3096/D/T/2005 tanggal 16 September 2005

                                Pada Tahun 2018 Semester Genap bertambah satu lagi Program Studi Pendidikan Teknologi Informasi Jenjang S1 yabg saat ini berada di Fakultas Ilmu Komputer dan Tahun 2020 bertambah Program Studi S2 yaitu Magister Ilmu Manajemen berada di Fakultas Ekonomi dan Magister Ilmu Biomedik dikelola oleh Fakultas Kedokteran. Sehingga saat ini Univesitas Methodist Indonesia mengelola 14 Progam Studi dengan 5 Fakultas.
